	idKH INT NOT NULL,
	hoKH VARCHAR (30) NOT NULL,
	tenKH VARCHAR (30),
	TPho VARCHAR (30) NOT NULL,
	vung VARCHAR (30) NOT NULL,
PRIMARY KEY (idKH));

CREATE TABLE KYNGHI (
idKH INT NOT NULL,
tenDD VARCHAR (30) NOT NULL,
ngayBD VARCHAR (10) NOT NULL,
ngayKT VARCHAR (10),
socho INT NOT NULL);


