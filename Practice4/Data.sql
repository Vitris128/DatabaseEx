CREATE TABLE DIADIEM (
tenDD VARCHAR (30) NOT NULL,
socho INT NOT NULL,
tpho VARCHAR (30) NOT NULL,
vung VARCHAR (30),
gia INT DEFAULT 0,
CONSTRAINT pk_hd PRIMARY KEY (tenDD));

CREATE TABLE HOATDONG (
	tenDD VARCHAR (30) NOT NULL,
